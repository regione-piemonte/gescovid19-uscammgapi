package it.csi.gescovid.uscammgapi.business.be.impl.mapper.dto.custom;

public class MedicoAura {

    private Integer idMedico;
    private String cfMedico;
    private String codiceReg;
    private String cognome;
    private String nome;
	public Integer getIdMedico() {
		return idMedico;
	}
	public void setIdMedico(Integer idMedico) {
		this.idMedico = idMedico;
	}
	public String getCfMedico() {
		return cfMedico;
	}
	public void setCfMedico(String cfMedico) {
		this.cfMedico = cfMedico;
	}
	public String getCodiceReg() {
		return codiceReg;
	}
	public void setCodiceReg(String codiceReg) {
		this.codiceReg = codiceReg;
	}
	public String getCognome() {
		return cognome;
	}
	public void setCognome(String cognome) {
		this.cognome = cognome;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}

    
 
}